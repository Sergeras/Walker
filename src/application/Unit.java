package application;



public class Unit {
	private double xPos, yPos;
	
	public Unit(double x, double y) {
		this.xPos = x;
		this.yPos = y;
	}

	public double getxPos() {
		return xPos;
	}

	public void setxPos(double xPos) {
		this.xPos = xPos;
	}

	public double getyPos() {
		return yPos;
	}

	public void setyPos(double yPos) {
		this.yPos = yPos;
	}

	
			
}
